void DiagCan_DiagCanValues( LdcDiagnosisType* pDiagFlags )
{
	uint8_t canStatus = RTE_GET_CAN_BUS_OFF_STATE();
	/* hk.im : uint8_t vcuReadyStatus = RTE_GET_CMD_VCU_READY(); */

	if( canStatus == yTRUE )
	{
		pDiagFlags->Diag_CanBusOffSts = yTRUE; /* no bus-off */
	}
	else
	{
		pDiagFlags->Diag_CanBusOffSts = yFALSE;
	}
}

